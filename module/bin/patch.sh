echo -e "\n"
tmp="$1"
pcm="$2"
amp="$3"

echo "- Cleaning up old mod paths"
rm -rf "$tmp/system" >&2
echo "- Copying mixer paths"
cp "/vendor/etc/mixer_paths.xml" "$tmp/mixer_paths.xml"
echo "- Patching mixer paths"
sed -r 's/AMP PCM Gain" value="(.*)"/AMP PCM Gain" value="'$amp'"/g; s/Digital PCM Volume" value="(.*)"/Digital PCM Volume" value="'$pcm'"/g; s/Boost Peak Current Limit" value="(.*)A"/Boost Peak Current Limit" value="3.50A"/g' "$tmp/mixer_paths.xml" > "$tmp/mixer_paths2.xml"
echo "- Creating mod paths"
mkdir -p "$tmp/system/vendor/etc"
echo "- Installing mixer paths"
mv "$tmp/mixer_paths2.xml" "$tmp/system/vendor/etc/mixer_paths.xml"
echo "- Cleaning up leftovers"
rm "$tmp/mixer_paths.xml"
rm -rf "/data/local/tmp/ptad"
